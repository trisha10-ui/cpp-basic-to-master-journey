//Arethmatic Operations on (int) data Type (+,-,*,/)
#include<iostream>
using namespace std;
int main(){
	int x=10;
	int y=5;

	cout<<"Addition: "<<x+y<<endl;    //addition
	cout<<"Substraction: "<<x-y<<endl;    //substraction
	cout<<"Multiplication: "<<x*y<<endl;   //multiplication
	cout<<"Division: "<<x/y<<endl;       //division
	return 0;
}
